#include "../../include/cub3d_bonus.h"

void	ft_addback(t_casting **lst, t_index ray, double distance, int id)
{
	t_casting	*new;
	t_casting	*tmp;

	tmp = *lst;
	new = (t_casting *)malloc(sizeof(t_casting));
	if (!new)
		return ;
	new->distance = distance;
	new->ray = ray;
	new->ray_id = id;
	new->next = NULL;
	if (*lst == NULL)
		*lst = new;
	else
	{
		while (tmp->next != NULL)
			tmp = tmp->next;
		tmp->next = new;
	}
}
